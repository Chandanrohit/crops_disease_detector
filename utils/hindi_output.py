# ── Public API ──────────────────────────────────────────────────────────────────

def get_advice(class_name: str) -> dict:
    """
    Return full Hindi advice dict for a predicted class name.
    Falls back gracefully for unknown classes.
    """
    data = DISEASE_DATA.get(class_name)
    if data is None:
        # Graceful fallback for any unlisted class
        severity = 2
        return {
            "hindi_name":     class_name.replace("___", " — ").replace("_", " "),
            "symptoms_hindi": "विवरण उपलब्ध नहीं है",
            "treatment":      ["🔬 निकटतम कृषि विज्ञान केंद्र (KVK) से सलाह लें"],
            "prevention":     "नियमित निगरानी जारी रखें।",
            "urgency":        "⚡ कृषि विशेषज्ञ से परामर्श करें",
            "severity":       severity,
            "severity_emoji": SEVERITY_EMOJI[severity],
            "severity_label": SEVERITY_LABEL[severity],
        }
    return {
        **data,
        "severity_emoji": SEVERITY_EMOJI[data["severity"]],
        "severity_label": SEVERITY_LABEL[data["severity"]],
    }


def list_all_diseases() -> list[str]:
    """Return all supported class names."""
    return list(DISEASE_DATA.keys())


